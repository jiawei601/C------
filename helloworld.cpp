#include <iostream>
// 引入输入输出流库，让程序可以进行屏幕输出等操作
// 主函数，程序的入口点，所有C++程序都必须有且仅有一个main函数
int main() {
    //std:: 表示这些内容属于标准命名空间
    std::cout << "Hello, World!"<< std::endl;

    // Indicate the C++ standard version used
    //count 表示用于输出内容到屏幕
    //endl 表示输出一个换行符，并刷新输出缓冲区
    //<<：输出运算符，将右边的内容传递给左边的输出对象
    /*
    std::cout是 C++ 标准库中的标准输出流对象（对应屏幕输出），
    输出运算符<<的作用是将右侧的 “数据” 插入到左侧的输出流中（简单说就是把数据打印到屏幕）。
    第一个<<：std::cout << "Hello, World!"
    左侧操作数：std::cout（输出流对象）
    右侧操作数："Hello, World!"（字符串常量，要输出的数据）
    作用：将字符串"Hello, World!"插入到std::cout输出流中，本质是把这个字符串打印到屏幕。
    返回值：这个操作会返回std::cout对象本身（这是链式调用的关键）。
     第二个<<：(std::cout << "Hello, World!") << std::endl
    左侧操作数：第一个<<执行后返回的std::cout对象（注意：这里是隐藏的，因为链式调用省略了括号）
    右侧操作数：std::endl（标准库中的「换行操纵符」，作用是换行 + 刷新输出缓冲区）
    作用：将std::endl插入到std::cout输出流中，本质是让屏幕输出换行，并确保数据立刻显示在屏幕上。
    返回值：同样返回std::cout对象（所以还能继续加<<实现更多连续输出）。
    这就像你往一个 “输出管道”（std::cout）里连续塞东西：




    int 表示函数执行结束后会返回一个整数，用于向操作系统标识程序的执行状态。通常约定：
返回 0 表示程序正常结束；
返回非 0 值表示程序异常结束（具体含义可自定义，通常用于标识错误类型）。
不过代码中 return 0.0; 是不规范的，因为 0.0 是浮点型，而函数声明的返回值类型是 int（整数型），正确写法应为 return 0;。
参数列表：() 中可以指定参数，常见形式有两种：
int main()：表示函数没有参数（代码中使用的就是这种形式）；
int main(int argc, char* argv[])：用于接收命令行参数，argc 是参数个数，argv 是参数数组。
函数体：{} 中的内容是函数的执行逻辑，包含了程序要完成的具体操作（如代码中的输出语句）。
    
    */
    std::cout << "This program is compiled with C++14 standard." << std::endl;
    return 0;
}
